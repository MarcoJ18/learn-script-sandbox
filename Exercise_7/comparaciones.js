// ¿Cuál será el resultado de las siguientes expresiones?

alert(5 > 4); // true
alert("apple" > "pineapple"); // false
alert("2" > "12"); // true porque no hay un numero para que se comparen como numero
alert(undefined == null); // true
alert(undefined === null); // false
alert(null == "\n0\n"); // false
alert(null === +"\n0\n"); // false
